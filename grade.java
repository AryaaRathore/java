public class grade {
    public static void main(String[] args) {
        int m = 90;
        if(m>=90){
            System.out.println("grade A");
        }
        else if (m>=70) {
            System.out.println("grade B");
            
        }
        else if (m>=65) {
            System.out.println("C");
        }
        else if (m>=35) {
            System.out.println("pass");
            
        }
        else{
            System.out.println("fail");
        }

    }
    
}
